def calc_new_height():
    width = int(input("Enter the current width: "))
    height = int(input("Enter the current width: "))
    desired_width = int(input("Enter the current width: "))
    corresponding_height = height / width * desired_width
    print("The corresponding height is:", corresponding_height)

calc_new_height()